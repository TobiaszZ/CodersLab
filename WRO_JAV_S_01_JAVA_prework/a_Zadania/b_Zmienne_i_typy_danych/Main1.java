
public class Main1 {
	public static void main(String[] args) {
  
  //Ta zmienna przechowuje typ int 4 bajty (zakres od -2147483648 do
  //  2147483647)
    int i = 2147483647;
    System.out.println(i);
    
  // Ta zmienna przechowuje typ char char - służy do reprezentacji
  //  pojedynczego znaku Unicode
    char c = 'c';
    System.out.println(c);
    
  // Ta zmienna przechowuje typ long 8 bajtów (zakres od -2^63 do (2^63)-1
    long l = 1000000000;
    System.out.println(l);
    
  // Ta zmienna przechowuje typ double 8 bajtów (ok 15 cyfr po przecinku)
    double d = 0.000009;
    System.out.println(d);		
    
  // Ta zmienna przechowuje typ boolean służy do przedstawiania wartości
  //  logicznej prawda lub fałsz
    boolean T = true;
    System.out.println(T);
}
}